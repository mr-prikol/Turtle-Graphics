/**
 * This package contains tools for creating simple 2D graphics
 * with the help of {@link Turtle} and {@link TurtleWindow} classes.
 * 
 * {@code turtlegraphics} includes the following classes:
 * <ul>
 * <li>{@link Turtle} - an object that moves in {@code TurtleScreen}
 * and paints on it.</li>
 * <li>{@link TurtleScreen} - represents 2D space in which the
 * turtle is painting. Connects the {@code Turtle} with the
 * {@code TurtleWindow}.</li>
 * <li>{@link Pen} - the {code Turtle}'s painting instrument.
 * It determines in which color and with what width
 * the {@code Turtle} will paint.</li>
 * <li>{@link TurtleWindow} - stores the window and all the drawings.</li>
 * </ul>
 * 
 * @version 1.0
 * @author MrPrikol
 */
package turtlegraphics;
